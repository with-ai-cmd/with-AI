def create_doc():
    doc = Document()
    section = doc.sections[0]
    section.page_width = Cm(21.0)
    section.page_height = Cm(29.7)
    section.top_margin = Cm(2.5)
    section.bottom_margin = Cm(2.5)
    section.left_margin = Cm(2.5)
    section.right_margin = Cm(2.5)
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Yu Mincho'
    font.size = Pt(10.5)
    rPr = style.element.get_or_add_rPr()
    rFonts = rPr.get_or_add_rFonts()
    rFonts.set(qn('w:eastAsia'), 'Yu Mincho')
    return doc

def set_run_font(run, bold=False, size=None):
    run.font.name = 'Yu Mincho'
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.get_or_add_rFonts()
    rFonts.set(qn('w:eastAsia'), 'Yu Mincho')
    if bold:
        run.bold = True
    if size:
        run.font.size = size

def add_title(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.space_after = Pt(24)
    p.space_before = Pt(12)
    run = p.add_run(text)
    set_run_font(run, bold=True, size=Pt(16))

def add_article_title(doc, text):
    p = doc.add_paragraph()
    p.space_before = Pt(18)
    p.space_after = Pt(6)
    run = p.add_run(text)
    set_run_font(run, bold=True, size=Pt(11))

def add_body(doc, text):
    p = doc.add_paragraph()
    p.space_before = Pt(2)
    p.space_after = Pt(2)
    run = p.add_run(text)
    set_run_font(run, size=Pt(10.5))

def add_numbered(doc, num, text):
    p = doc.add_paragraph()
    p.space_before = Pt(2)
    p.space_after = Pt(2)
    pf = p.paragraph_format
    pf.left_indent = Cm(0.5)
    pf.first_line_indent = Cm(-0.5)
    run = p.add_run(f'{num}. {text}')
    set_run_font(run, size=Pt(10.5))

def add_bullet(doc, text, indent=1.0):
    p = doc.add_paragraph()
    p.space_before = Pt(1)
    p.space_after = Pt(1)
    pf = p.paragraph_format
    pf.left_indent = Cm(indent)
    pf.first_line_indent = Cm(-0.3)
    run = p.add_run(f'・{text}')
    set_run_font(run, size=Pt(10.5))

def add_empty_line(doc, count=1):
    for _ in range(count):
        p = doc.add_paragraph()
        p.space_before = Pt(0)
        p.space_after = Pt(0)
        run = p.add_run('')
        run.font.size = Pt(6)

def add_table(doc, rows_data):
    table = doc.add_table(rows=len(rows_data), cols=2)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = 'Table Grid'
    for i, (col1, col2) in enumerate(rows_data):
        cell0 = table.cell(i, 0)
        cell1 = table.cell(i, 1)
        cell0.width = Cm(3.5)
        cell1.width = Cm(12.5)
        run0 = cell0.paragraphs[0].add_run(col1)
        set_run_font(run0, bold=True, size=Pt(10))
        run1 = cell1.paragraphs[0].add_run(col2)
        set_run_font(run1, size=Pt(10))

def add_signature_block(doc, label, items, with_seal=True):
    p = doc.add_paragraph()
    p.space_before = Pt(12)
    p.space_after = Pt(6)
    run = p.add_run(label)
    set_run_font(run, bold=True, size=Pt(11))
    for key, val in items:
        p2 = doc.add_paragraph()
        p2.space_before = Pt(2)
        p2.space_after = Pt(2)
        p2.paragraph_format.left_indent = Cm(0.5)
        run2 = p2.add_run(f'{key}：{val}')
        set_run_font(run2, size=Pt(10.5))
    if with_seal:
        p3 = doc.add_paragraph()
        p3.space_before = Pt(8)
        p3.paragraph_format.left_indent = Cm(0.5)
        run3 = p3.add_run('印：＿＿＿＿＿＿＿＿')
        set_run_font(run3, size=Pt(10.5))

