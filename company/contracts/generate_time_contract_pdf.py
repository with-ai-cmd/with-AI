class ContractPDF(FPDF):
    def __init__(self):
        super().__init__()
        self.add_font("IPA", "", FONT_PATH)
        self.set_auto_page_break(auto=True, margin=25)

    def header(self):
        pass

    def footer(self):
        self.set_y(-15)
        self.set_font("IPA", "", 8)
        self.cell(0, 10, f"- {self.page_no()} -", align="C")

    def add_title(self, text):
        self.set_font("IPA", "", 16)
        self.cell(0, 12, text, align="C", new_x="LMARGIN", new_y="NEXT")
        self.ln(8)

    def add_heading(self, text):
        self.ln(4)
        self.set_font("IPA", "", 12)
        self.cell(0, 8, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def add_body(self, text):
        self.set_font("IPA", "", 10)
        self.multi_cell(0, 6, text)
        self.ln(1)

    def add_body_indent(self, text, indent=8):
        self.set_font("IPA", "", 10)
        x = self.get_x()
        w = self.w - self.r_margin - x - indent
        self.set_x(x + indent)
        self.multi_cell(w, 6, text)
        self.ln(1)

    def add_table(self, rows, col_widths=None):
        self.set_font("IPA", "", 10)
        if col_widths is None:
            col_widths = [50, 120]
        for row in rows:
            for i, cell in enumerate(row):
                w = col_widths[i] if i < len(col_widths) else 50
                self.cell(w, 7, cell, border=1)
            self.ln()
        self.ln(2)

    def add_separator(self):
        self.ln(2)


